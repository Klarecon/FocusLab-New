/**
 * Rewrite every ClickUp task into plain, non-technical language.
 * Updates the task title + description ("What it is / Why it matters") and
 * replaces its checklist with plain steps (tech term in brackets).
 * Throttled to stay under ClickUp's rate limit.
 * Usage: node scripts/clickup/plain-language.mjs
 */
import { readFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const API = "https://api.clickup.com/api/v2";
const TOKEN = (() => {
  if (process.env.CLICKUP_TOKEN) return process.env.CLICKUP_TOKEN.trim();
  const env = readFileSync(join(__dirname, "..", "..", ".env.local"), "utf8");
  return env.match(/CLICKUP_TOKEN=(.+)/)[1].trim().replace(/^["']|["']$/g, "");
})();
const { tasks } = JSON.parse(readFileSync(join(__dirname, "task-map.json"), "utf8"));
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

async function api(method, path, body) {
  const res = await fetch(API + path, {
    method,
    headers: { Authorization: TOKEN, "Content-Type": "application/json" },
    body: body ? JSON.stringify(body) : undefined,
  });
  if (!res.ok) throw new Error(`${method} ${path} → ${res.status} ${await res.text()}`);
  await sleep(650); // throttle: ~90 req/min
  return res.status === 204 ? {} : res.json();
}

