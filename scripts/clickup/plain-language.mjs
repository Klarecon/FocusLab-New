async function apply(d) {
  const id = tasks[d.key];
  if (!id) { console.error(`✗ ${d.key}: not in map`); return; }
  const desc = `**What it is:** ${d.what}\n\n**Why it matters:** ${d.why}\n\n*${d.meta}*`;
  // update title + description
  await api("PUT", `/task/${id}`, { name: `[${d.key}] ${d.title}`, description: desc });
  // replace checklist: delete existing, add fresh plain one
  const task = await api("GET", `/task/${id}`);
  for (const cl of task.checklists || []) await api("DELETE", `/checklist/${cl.id}`);
  const { checklist } = await api("POST", `/task/${id}/checklist`, { name: "Steps" });
  for (const item of d.steps) await api("POST", `/checklist/${checklist.id}/checklist_item`, { name: item });
  console.log(`✓ ${d.key}  ${d.title}`);
}

let n = 0;
for (const d of DATA) { await apply(d); n++; }
console.log(`\nDone. ${n} tasks rewritten in plain language.`);
